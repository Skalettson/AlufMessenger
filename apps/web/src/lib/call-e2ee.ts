function normalizeFingerprintHex(line: string): string {
  return line.trim().replace(/:/g, '').toLowerCase();
}

/** Извлекает первый отпечаток из SDP (предпочтительно SHA-256). */
export function extractFingerprintFromSdp(sdp: string): string | null {
  const m256 = sdp.match(/a=fingerprint:sha-256\s+([0-9a-fA-F:\s]+)/i);
  if (m256?.[1]) return normalizeFingerprintHex(m256[1]);
  const m384 = sdp.match(/a=fingerprint:sha-384\s+([0-9a-fA-F:\s]+)/i);
  if (m384?.[1]) return normalizeFingerprintHex(m384[1]);
  const m1 = sdp.match(/a=fingerprint:sha-1\s+([0-9a-fA-F:\s]+)/i);
  if (m1?.[1]) return normalizeFingerprintHex(m1[1]);
  return null;
}

/**
 * Одна и та же строка у обоих участников при успешном DTLS.
 * Не передаётся по сети отдельно — вычисляется локально из SDP.
 */
export async function deriveCallVerificationEmoji(localSdp: string, remoteSdp: string): Promise<string> {
  const a = extractFingerprintFromSdp(localSdp);
  const b = extractFingerprintFromSdp(remoteSdp);
  if (!a || !b) return '';

  const sorted = [a, b].sort().join('|');
  const data = new TextEncoder().encode(sorted);
  const hash = new Uint8Array(await crypto.subtle.digest('SHA-256', data));

  const n = EMOJI_POOL.length;
  const out: string[] = [];
  for (let i = 0; i < 4; i++) {
    out.push(EMOJI_POOL[hash[i]! % n]!);
  }
  return out.join(' ');
}
